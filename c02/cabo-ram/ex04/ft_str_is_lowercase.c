#include <stdio.h>

int	ft_str_is_lowercase(char *str)
{
	int	i;

	i = 0;
	if (str[i] == '\0')
		return (1);
	while (str[i] != '\0')
	{
		if (!(str[i] >= 'a' && str[i] <= 'z'))
			return (0);
		i++;
	}
	return (1);
}

int main(int argc, char **argv) {
	int n;

	if (argc < 2) return 1;
	n = ft_str_is_lowercase(argv[1]);
	printf("is lowercase? %i", n);
	return 0;
}

