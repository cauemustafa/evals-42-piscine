#include <unistd.h>
#include <stdio.h>

int	ft_str_is_numeric(char *str)
{
	int	i;

	i = 0;
	if (str[i] == '\0')
		return (1);
	while (str[i] != '\0')
	{
		if (!(str[i] >= '0' && str[i] <= '9'))
			return (0);
		i++;
	}
	return (1);
}

int main(int argc, char **argv) {
	int n;

	if (argc < 2) return 1;
	n = ft_str_is_numeric(argv[1]);
	printf("is numeric? %i", n);
	return 0;
}
