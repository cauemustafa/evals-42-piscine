int	ft_atoi(char *str)
{
	int	n_b;
	int	n;
	int	i;

	n_b = 0;
	n = 0;
	i = 0;
	while (*str)
	{
		if (n_b == 0 && *str == '-')
			i++;
		else if (*str >= '0' && *str <= '9')
		{
			n = (n * 10) + ((int) *str - 48);
			n_b = 1;
		}
		else if ((!(*str >= '0' && *str <= '9') && n_b == 1)
			|| (*str != '+' && *str != ' ' && !(*str >= '0' && *str <= '9')))
			break ;
		str++;
	}
	if (i % 2 == 1)
		n *= -1;
	return (n);
}

#include <stdio.h>
int main(void)
{
    printf("%d", ft_atoi(" ---+--+1234ab567"));
}
