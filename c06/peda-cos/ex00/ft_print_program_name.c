#include <unistd.h>

void	ft_putchar(char c)
{
	write(1, &c, 1);
}

void	ft_putstr(char *str)
{
	while (*str)
		ft_putchar(*str++);
}

int	main(int argc, char **argv)
{
	char	*x;

	(void)argc;
	(void)argv;
	x = argv[0];
	ft_putstr(x);
	return (0);
}
